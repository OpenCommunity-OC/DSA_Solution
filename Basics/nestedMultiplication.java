package Basics;

public class nestedMultiplication {
    public static void main(String[] args) {
        
        //Mandatory Range from 1 to 10
        for (int i = 1; i <= 20; i++) {
            for (int j = 0; j <=10; j++) {
                System.out.println(i+"X"+j+"="+i*j);

                System.out.println();

            }
            
        }
    }
 
}
